//import mongoose
const mongoose = require("mongoose");

//route handler
const postSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  body: {
    type: String,
    required: true,
  },
  //likes and comments contain an array
  //because there are multiple like and multiple comment
  likes: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Like",
    },
  ],
  comments: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Comment",
    },
  ],
});
//export
module.exports = mongoose.model("Post", postSchema);
